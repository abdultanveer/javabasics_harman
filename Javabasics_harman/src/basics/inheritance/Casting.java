package basics.inheritance;

public class Casting {
	
	public static void main(String[] args) {
		float pi = 3.14f;
		System.out.println("the value of pi is--"+ pi);
		int py = (int)pi; //convert this float into int
		System.out.println("the value of py is--"+ py);
		float pye = py;
		System.out.println("the value of pye is--"+ pye);


	}

}
